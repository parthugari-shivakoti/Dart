void main() {
    // Example of a for loop
    print('For loop example:');
    for (int i = 0; i < 5; i++) {
        print('Iteration $i');
    }

    // Example of a while loop
    print('\nWhile loop example:');
    int j = 0;
    while (j < 5) {
        print('Iteration $j');
        j++;
    }
}